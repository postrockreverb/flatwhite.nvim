#!/usr/bin/env python3
"""flatwhite — theme generator.

Single source of truth for the flatwhite colorscheme across apps.
Run `python3 build.py` to (re)generate:

  lua/flatwhite/palette.lua        Lua palette table
  kitty/flatwhite.conf             kitty theme
  fish/flatwhite.fish              fish shell colors
  bat/flatwhite.tmTheme            Sublime tmTheme (used by bat / delta)
  assets/palette.svg               palette reference card (README)

Neovim highlight logic lives in lua/flatwhite/init.lua and is NOT
generated — it references the keys defined here.
"""

